
public class Tv {
	boolean power;
	int channel;
	int volum;

	void power() {
		power = !power;
	}
	
	void channelUp() {
		channel++;
	}

	void channelDown() {
		channel--;
	}
	
	void volumUp() {
		volum++;
	}
	
	void volumDown() {
		volum--;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
