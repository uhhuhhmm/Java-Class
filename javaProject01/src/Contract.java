
public class Contract {
	String name;
	int age;
	int address;
	int weight;
	int height;
	public Contract() {

	}
	
	public Contract(String name, int age, int address, int weight, int height) {
		this.name = name;
		this.age = age;
		this.address = address;
		this.weight = weight;
		this.height = height;
	}
	
	
	
}
